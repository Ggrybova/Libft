#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char *a;

	a = (char*)s;
	if (c == '\0')
	{
		while (*a != '\0')
			a++;
		return (a++);
	}
	while (*a)
	{
		if (*a == c)
			return (a++);
		a++;
	}
	return (NULL);
}
